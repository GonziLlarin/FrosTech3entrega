class Helados {
    constructor(tipo, sabor, precio, stock, img) {
        this.tipo = tipo // cucurucho / bombon / torta y cafe / tortas heladas
        this.sabor = sabor //gusto del helado 
        this.precio = Number(precio) //precio del helado
        this.stock = stock //cant de helados
        this.img = img
    }
}